package ssg.search.result;

public class Size {
	private String sizeCd;
	private String sizeNm;
	private String sizeCount;
	
	public String getSizeCd() {
		return sizeCd;
	}
	public void setSizeCd(String sizeCd) {
		this.sizeCd = sizeCd;
	}
	public String getSizeNm() {
		return sizeNm;
	}
	public void setSizeNm(String sizeNm) {
		this.sizeNm = sizeNm;
	}
	public String getSizeCount() {
		return sizeCount;
	}
	public void setSizeCount(String sizeCount) {
		this.sizeCount = sizeCount;
	}
}
